    private void jTextFieldAdicRemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextFieldAdicRemActionPerformed
        if (!jTextFieldAdicRem.getText().trim().equals("")) {
            if (jRadioButtonAdd.isSelected()) {
                jTextFieldAdicRem.setText(jTextFieldAdicRem.getText().trim().replace("-", ""));
                jTextFieldQuantFutura.setText(Integer.parseInt(jTextFieldQuantAtual.getText().trim()) + Integer.parseInt(jTextFieldAdicRem.getText().trim()) + "");
                // Teste valor negativo
                if(Integer.parseInt(jTextFieldQuantFutura.getText().trim()) < 0){
                    JOptionPane.showMessageDialog(null, "Valor futuro não pode ser negativo.");
                    jTextFieldQuantFutura.setText("");
                }
                    
            } else {
                jTextFieldAdicRem.setText(jTextFieldAdicRem.getText().trim().replace("+", ""));
                jTextFieldQuantFutura.setText(Integer.parseInt(jTextFieldQuantAtual.getText().trim()) - Integer.parseInt(jTextFieldAdicRem.getText().trim()) + "");
                // Teste valor negativo
                if(Integer.parseInt(jTextFieldQuantFutura.getText().trim()) < 0){
                    JOptionPane.showMessageDialog(null, "Valor futuro não pode ser negativo.");
                    jTextFieldQuantFutura.setText("");
                }
            }
        }
    }//GEN-LAST:event_jTextFieldAdicRemActionPerformed

