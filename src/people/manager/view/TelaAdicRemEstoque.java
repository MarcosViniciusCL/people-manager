    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jRadioButtonAdd.isSelected() && !jTextFieldQuantFutura.getText().trim().equals("")) {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que você quer ADICIONAR " + jTextFieldAdicRem.getText().trim() + " ao\nproduto com ID: " + jTextFieldID.getText().trim() + "?") == 0) {
                ControllerProduto.adicionaEstoque(Integer.parseInt(jTextFieldID.getText().trim()), Integer.parseInt(jTextFieldAdicRem.getText().trim()));
                Controller.novoLog(" adicionou "+jTextFieldAdicRem.getText().trim()+" no estoque do produto ID: "+jTextFieldID.getText().trim());
                JOptionPane.showMessageDialog(null, "Foi adicionado a quantidade ao produto.");
                
            }
        } else if (!jRadioButtonAdd.isSelected() && !jTextFieldQuantFutura.getText().trim().equals("")) {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que você quer REMOVER " + jTextFieldAdicRem.getText().trim() + " ao\nproduto com ID:" + jTextFieldID.getText().trim() + "?") == 0) {
                ControllerProduto.removerEstoque(Integer.parseInt(jTextFieldID.getText().trim()), Integer.parseInt(jTextFieldAdicRem.getText().trim()));
                JOptionPane.showMessageDialog(null, "Foi removido a quantidade do produto.");
                Controller.novoLog(" retirou "+jTextFieldAdicRem.getText().trim()+" do estoque do produto ID: "+jTextFieldID.getText().trim());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Valor futuro deve estar preenchido.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

