    private boolean campoVazio() {
        if (jPasswordField1.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Campo \"Nova Senha\" deve ser preenchido.\nNão é permitido usuario sem senha.");
            return true;
        }
        return false;
    }

