    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
        if (jCheckBox1.isSelected()) {
            jPasswordField1.setEchoChar((char) 0);
        } else {
            jPasswordField1.setEchoChar('\u2022');
        }
    }//GEN-LAST:event_jCheckBox1ActionPerformed

    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        try {
            jLabelImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource("ajax-loader-barra.gif")));
            Usuario u = ControllerUsuario.obterUsuario(jTextField1.getText().trim());
            jComboBox1.setSelectedIndex(u.getLevel());
            jLabelImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource("check-mark.png")));
        } catch (UsuarioNaoExisteException ex) {
            jLabelImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource("error.png")));
            JOptionPane.showMessageDialog(null, "Usuario não encontrado.\nVerifique o nome escrito.");
        }

    }//GEN-LAST:event_jTextField1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (!campoVazio() && ControllerUsuario.obterUsuario(jTextField1.getText().trim()) != null) {
                ControllerUsuario.editarUsuario(jTextField1.getText().trim(), new String(jPasswordField1.getPassword()), jComboBox1.getSelectedIndex());
                JOptionPane.showMessageDialog(null, "Usuario alterado.");
                Controller.novoLog("trocou a senha do usuario '"+jTextField1.getText().trim()+"', estado atual: "+jComboBox1.getSelectedItem());
                dispose();
            }
        } catch (UsuarioNaoExisteException ex) {
            JOptionPane.showMessageDialog(null, "Usuario não encontrado.\nVerifique o nome escrito.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

