    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        new Thread() {

            @Override
            public void run() {
                ArrayList clientes;
                jLabelImagem.setIcon(new javax.swing.ImageIcon(getClass().getResource("ajax-loader-cicle.gif")));
                switch (jComboBoxTipoBusca.getSelectedIndex()) {
                    case 0:
                        try {
                            clientes = ControllerCliente.buscarCliente(0, jTextField1.getText().trim());
                            criarTabela(clientes);
                        } catch (ClienteNaoEncontradoException ex) {
                            JOptionPane.showMessageDialog(null, "Cliente não encontrado");
                        }
                        break;
                    case 1:
                        try {
                            clientes = ControllerCliente.buscarCliente(1, jTextField1.getText().trim());
                            criarTabela(clientes);
                        } catch (ClienteNaoEncontradoException ex) {
                            JOptionPane.showMessageDialog(null, "Cliente não encontrado");
                        }
                        break;
                    default:
                        try {
                            clientes = ControllerCliente.buscarCliente(2, jTextField1.getText().trim());
                            criarTabela(clientes);
                        } catch (ClienteNaoEncontradoException ex) {
                            JOptionPane.showMessageDialog(null, "Cliente não encontrado");
                        }
                        break;
                }
                jLabelImagem.setIcon(null);
            }
        }.start();
    }//GEN-LAST:event_jButton1ActionPerformed

